export function getSpellSlots(
  level: number,
  progression: SpellProgression
): Record<number, number> {
  const normalizedLevel = Math.max(1, Math.min(20, Math.floor(level || 1)));
  const table =
    progression === "full"
      ? FULL_CASTER_SLOTS
      : progression === "half"
        ? HALF_CASTER_SLOTS
        : THIRD_CASTER_SLOTS;

  return { ...(table[normalizedLevel] ?? {}) };
}
